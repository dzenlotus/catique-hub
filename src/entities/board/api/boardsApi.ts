/**
 * Boards IPC client.
 *
 * Wraps Tauri `invoke` calls for the `Board` aggregate. Argument shape
 * follows the contract Olga's Rust side accepts: keys are camelCase on
 * the JS side (Tauri auto-converts to snake_case for Rust per the
 * v2.x convention — see https://tauri.app/v2/concept/inter-process-communication/).
 *
 * Errors thrown by the underlying Rust handler arrive here as a JSON-
 * serialised `AppError` (see `bindings/AppError.ts`). We unwrap and
 * re-throw a typed `AppErrorInstance` so call-sites get a discriminated
 * union via `.error.kind` instead of `unknown`.
 *
 * The `Board` and `AppError` types are imported directly from the
 * ts-rs-generated bindings — never hand-edited, regenerated by
 * `cargo test -p catique-domain && cargo test -p catique-api`.
 */

import {
  AppErrorInstance,
  invokeWithAppError,
} from "@shared/api";
import type { Board } from "@bindings/Board";

/**
 * Re-export the shared `AppErrorInstance` so existing
 * `import { AppErrorInstance } from "@entities/board"` callers keep
 * working. New code should import from `@shared/api` directly
 * (audit-#17).
 */
export { AppErrorInstance };

// audit-#17: AppErrorInstance + invokeWithAppError + isAppErrorShape
